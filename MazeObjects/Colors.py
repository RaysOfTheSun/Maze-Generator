class Color:
    def __init__(self):
        self.black = (0, 0, 0)
        self.white = (255, 255, 255)
        self.blue = (0, 0, 255)
        self.purple = (128, 0, 128)
        self.green = (0, 128, 0)
        self.yellow = (255, 255, 0)

    @property
    def white(self):
        return self.white
